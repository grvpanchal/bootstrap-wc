// Generates src/bootstrap-css.ts from the installed bootstrap package so the
// string is bundled into @bootstrap-wc/core and can be adopted into every
// component's shadow root at runtime. Run at build time (pre-tsc).
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, '..', '..', '..');
const candidates = [
  path.join(repoRoot, 'node_modules', 'bootstrap', 'dist', 'css', 'bootstrap.min.css'),
  path.resolve(here, '..', 'node_modules', 'bootstrap', 'dist', 'css', 'bootstrap.min.css'),
];
const cssPath = candidates.find((p) => fs.existsSync(p));
if (!cssPath) {
  console.error('[@bootstrap-wc/core] could not locate bootstrap/dist/css/bootstrap.min.css');
  process.exit(1);
}
const css = fs.readFileSync(cssPath, 'utf8');
const outPath = path.resolve(here, '..', 'src', 'bootstrap-css.ts');
const banner = `// AUTO-GENERATED by scripts/generate-bootstrap-css.mjs. Do not edit by hand.\n// Source: bootstrap/dist/css/bootstrap.min.css\n`;
fs.writeFileSync(outPath, `${banner}export const BOOTSTRAP_CSS = ${JSON.stringify(css)};\n`);
console.log(`[@bootstrap-wc/core] wrote ${outPath} (${css.length} chars)`);
